# Tipo de dado bool (boolean)
# Ao questinar algo em um programa
# só existem duas respostas possíveis:
# sim (true) ou não (False).
# Existem vários operadores parar "questionar"
# Dentro eles o ==, que é um operador lógico que
# questiona se um valor é igual a outro
print(10 == 10) #sim => True (verdadeiro)
print(10 == 11) # Não => False (Falso)
print(type(True))
print(type(False))
print(type(10 == 10))
print(type(10 == 11))

